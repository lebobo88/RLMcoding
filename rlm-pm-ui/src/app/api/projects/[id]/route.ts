import { NextRequest, NextResponse } from "next/server";
import { db, projects } from "@/lib/db";
import { eq } from "drizzle-orm";

// GET /api/projects/[id] - Get a single project
export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;

    const project = await db
      .select()
      .from(projects)
      .where(eq(projects.id, id))
      .get();

    if (!project) {
      return NextResponse.json(
        { error: { code: "NOT_FOUND", message: "Project not found" } },
        { status: 404 }
      );
    }

    return NextResponse.json({ data: project });
  } catch (error) {
    console.error("Error fetching project:", error);
    return NextResponse.json(
      { error: { code: "DB_ERROR", message: "Failed to fetch project" } },
      { status: 500 }
    );
  }
}

// PUT /api/projects/[id] - Update a project
export async function PUT(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;
    const body = await request.json();
    const { name, githubRepo, githubBranch, status, currentPhase } = body;

    // Check if project exists
    const existing = await db
      .select()
      .from(projects)
      .where(eq(projects.id, id))
      .get();

    if (!existing) {
      return NextResponse.json(
        { error: { code: "NOT_FOUND", message: "Project not found" } },
        { status: 404 }
      );
    }

    // Build update object
    const updates: Record<string, unknown> = {
      updatedAt: new Date(),
    };

    if (name !== undefined) updates.name = name;
    if (githubRepo !== undefined) updates.githubRepo = githubRepo;
    if (githubBranch !== undefined) updates.githubBranch = githubBranch;
    if (status !== undefined) updates.status = status;
    if (currentPhase !== undefined) updates.currentPhase = currentPhase;

    // Update project
    await db.update(projects).set(updates).where(eq(projects.id, id));

    // Fetch updated project
    const updated = await db
      .select()
      .from(projects)
      .where(eq(projects.id, id))
      .get();

    return NextResponse.json({ data: updated });
  } catch (error) {
    console.error("Error updating project:", error);
    return NextResponse.json(
      { error: { code: "DB_ERROR", message: "Failed to update project" } },
      { status: 500 }
    );
  }
}

// DELETE /api/projects/[id] - Delete a project
export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;

    // Check if project exists
    const existing = await db
      .select()
      .from(projects)
      .where(eq(projects.id, id))
      .get();

    if (!existing) {
      return NextResponse.json(
        { error: { code: "NOT_FOUND", message: "Project not found" } },
        { status: 404 }
      );
    }

    // Delete project (doesn't delete local files)
    await db.delete(projects).where(eq(projects.id, id));

    return new NextResponse(null, { status: 204 });
  } catch (error) {
    console.error("Error deleting project:", error);
    return NextResponse.json(
      { error: { code: "DB_ERROR", message: "Failed to delete project" } },
      { status: 500 }
    );
  }
}
